#include<stdio.h>
void main()
{
	int no;
	printf("Enter a number:");
	scanf("%d",&no);
	if(no==1)
	{
		printf("ONE");
	}
	else if(no==2)
	{
		printf("TWO");
	}
	else if(no==3)
	{
		printf("THREE");
	}
	else if(no==4)
	{
		printf("FOUR");
	}
	else if(no==5)
	{
		printf("FIVE");
	}
	else
	{
		printf("INVALID NUMBER");
	}
}
